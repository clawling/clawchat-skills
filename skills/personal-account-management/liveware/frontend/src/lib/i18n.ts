const DICTIONARIES: Record<Language, Record<TranslationKey, string>> = {
  en: EN,
  zh: ZH,
};

export function languageFromSearch(search?: string): Language {
  const source = search ?? (typeof window === "undefined" ? "" : window.location.search);
  const requested = new URLSearchParams(source).get("lang")?.toLowerCase() ?? "";
  return requested.startsWith("zh") ? "zh" : "en";
}

export function translator(language: Language): (key: TranslationKey) => string {
  const dictionary = DICTIONARIES[language] ?? EN;
  return (key) => dictionary[key] ?? EN[key];
}

export function loadErrorDescriptionKey(error: DashboardLoadError | null): TranslationKey {
  if (error === "unsupported") return "loadErrorUnsupported";
  if (error === "invalid") return "loadErrorInvalid";
  return "loadErrorUnavailable";
}
